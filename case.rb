my_weather = 'rainy'

case my_weather
when 'sunny'
puts "Better get your shorts and sunblock out!"
when 'rainy'
puts "Fine weather for ducks! Wear the boots."
when 'snowing'
puts "We didn't move south for this! Go to Kroger and buy all the milk and bread."
when 'cloudy'
puts "Put away the shades, babe. Wear the black to match your soul."
else
puts "We have no idea what the weather is but be sure to put on pants!"
end